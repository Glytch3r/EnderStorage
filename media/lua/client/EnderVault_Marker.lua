require "lua_timers"
EnderVault = EnderVault or {}

EnderVault.col = ColorInfo.new(0.38, 0.86, 0.78, 1);
EnderVault.marker = nil
EnderVault.color = { r = 0.38, g = 0.86, b = 0.78, a = 1 }

-----------------------            ---------------------------
function EnderVault.tempMark(sq)
    if not sq then return end
    EnderVault.addMark(sq)
    timer:Simple(30, function()
        EnderVault.delMark()
    end)
end

-----------------------            ---------------------------
function EnderVault.addMark(sq)
    local pl = getPlayer()
    if sq then
        if EnderVault.marker ~= nil then
            EnderVault.delMark()
        end
        if EnderVault.marker == nil then
            EnderVault.marker = getWorldMarkers():addGridSquareMarker("EnderVault", "",  sq, EnderVault.color.r, EnderVault.color.g, EnderVault.color.b,  true,  4)

            --EnderVault.marker = getWorldMarkers():addPlayerHomingPoint(pl, sq:getX(), sq:getY(), "EnderVault", EnderVault.color.r, EnderVault.color.g, EnderVault.color.b, EnderVault.color.a, true, 60);
        end
    end
end

function EnderVault.delMark()
    if EnderVault.marker  ~= nil then
        EnderVault.marker:remove()
        EnderVault.marker = nil
    end
end
-----------------------            ---------------------------



function EnderVault.delayRemoveHL(pl, obj, sq)
    function EnderVault.doDelayRemoveHL()
        if EnderVault.checkDist(pl, sq) then
            print("doDelayRemoveHL")
            EnderVault.setHL(obj, false)
            Events.OnTick.Remove(EnderVault.doDelayRemoveHL)
        end
    end
    Events.OnTick.Add(EnderVault.doDelayRemoveHL)
end

function EnderVault.setHL(obj, bool)
    local pl = getPlayer()
    if not obj then return end
    local col = EnderVault.col
    obj:setHighlightColor(col);
    obj:setOutlineHighlightCol(col);
    local sq = obj:getSquare()
    if bool then
        EnderVault.delayRemoveHL(pl, obj, sq)
    end
    obj:setOutlineThickness(0.6);

    obj:setBlink(bool);
    obj:setOutlineHlBlink(bool);
    obj:setOutlineHighlight(bool);
    obj:setHighlighted(bool);
end