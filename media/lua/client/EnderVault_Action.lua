EnderVault = EnderVault or {}
require "TimedActions/ISBaseTimedAction"

EnderVault.Action = ISBaseTimedAction:derive("EnderVault.Action")

function EnderVault.Action:update()
    self.character:faceThisObject(self.vault)
end

function EnderVault.Action:isValid()
    return self.vault and self.vault:getSquare() ~= nil
end

function EnderVault.Action:waitToStart()
    self.character:faceThisObject(self.vault)
    return self.character:shouldBeTurning()
end

function EnderVault.Action:start()
    self:setActionAnim("RemoveBarricade")
    self:setAnimVariable("RemoveBarricade", "CrowbarMid")
    self:setOverrideHandModels(self.character:getPrimaryHandItem(), self.character:getSecondaryHandItem())
    self.sound = self.character:playSound("BeginRemoveBarricadeMetal")
    addSound(self.character, self.character:getX(), self.character:getY(), self.character:getZ(), 10, 1)
end

function EnderVault.Action:stop()
    self:_stopSound()
    ISBaseTimedAction.stop(self)
end
function EnderVault.Action:perform()
    self:_stopSound()

    if self.isTake then
        if EnderVault.isEnderVault(self.vault) then
            EnderVault.storeData(self.vault:getContainer(), true)
        end
        self:_sledgeVault()
    else
        self:_convertVault()
    end

    self.character:playSound("RemoveBarricadeMetal")
    ISBaseTimedAction.perform(self)
end

function EnderVault.Action:_stopSound()
    if self.sound then
        self.character:getEmitter():stopSound(self.sound)
        self.sound = nil
    end
end

function EnderVault.Action:_convertVault()
    local sprName = EnderVault.getSprName(self.vault)
    if sprName then
        EnderVault.doConvert(self.vault, sprName)
    end
end

function EnderVault.Action:_sledgeVault()
    EnderVault.doSledge(self.vault)
    local inv = self.character:getInventory()
    if inv then
        inv:AddItem("Base.EnderVault")
    end
end


-----------------------            ---------------------------


function EnderVault.Action:new(character, vault, isTake)
    local o = {}
    setmetatable(o, self)
    self.__index = self
    o.character = character
    o.vault = vault
    o.isTake = isTake
    o.stopOnWalk = false
    o.stopOnRun = true
    o.maxTime = 120
    return o
end
